#pragma once

template <class T>
class BasicBuilder
{
	private:
		// -------------------------------------------------------------- //
		//  Attributes                                                    //
		// -------------------------------------------------------------- //
		T _object;

	public:
		// -------------------------------------------------------------- //
		//  Constructors & Destructors                                    //
		// -------------------------------------------------------------- //
		BasicBuilder(void):
			_object()
		{}

		~BasicBuilder(void) {}

		// -------------------------------------------------------------- //
		//  Public Methods                                                //
		// -------------------------------------------------------------- //

		T* build(void)
		{
			return new T(_object);
		}
};

